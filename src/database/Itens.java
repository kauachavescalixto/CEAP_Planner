package database;

public class Itens {
	private int id;
	private String nome, predio;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getPredio() {
		return predio;
	}
	public void setPredio(String email) {
		this.predio = email;
	}

	
}
